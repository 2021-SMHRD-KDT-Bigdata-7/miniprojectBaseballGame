package BaseBallGame;

public class AllPlayer {

	
	private int id;
	private String name;
	private int stat;
	
	public void player() {
		
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public int getStat() {
		return stat;
	}

	@Override
	public String toString() {
		return "Allplayer [id=" + id + ", name=" + name + ", stat=" + stat + "]";
	}
	
	


}
